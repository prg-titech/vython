# 数値の演算
num1 = 15
num2 = 4

add_result = num1 + num2
sub_result = num1 - num2
mul_result = num1 * num2
div_result = num1 / num2

print(add_result) # 19
print(sub_result) # 11
print(mul_result) # 60
print(div_result) # 3.75

# 文字列の演算
str1 = "Hello"
str2 = "World"

concat_result = str1 + " " + str2
print(concat_result) # "Hello World"

# 論理演算
bool1 = True
bool2 = False

and_result = bool1 and bool2
or_result = bool1 or bool2
not_result = not bool1

print(and_result) # False
print(or_result)  # True
print(not_result) # False

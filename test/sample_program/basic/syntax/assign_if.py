# 変数代入
a = 10
b = 5

# if文による条件分岐
if a > b:
    result = "a is greater than b"
elif a < b:
    result = "a is less than b"
else:
    result = "a is equal to b"

# 結果の出力
print(result)  # "a is greater than b"

# 別の条件分岐
c = 3
d = 7

if c > d:
    result = "c is greater than d"
elif c < d:
    result = "c is less than d"
else:
    result = "c is equal to d"

# 結果の出力
print(result)  # "c is less than d"
